
namespace Tic_Tac_Toe
{
    public enum MarkType
    {
        /// <summary>
        /// An empty cell
        /// </summary>
        Free,

        /// <summary>
        /// A player mark (An 'X' is displayed when a player plays)
        /// </summary>
        Cross,

        /// <summary>
        /// A computer mark ('O' is displayed after computer plays)
        /// </summary>
        Nought
    }
}
